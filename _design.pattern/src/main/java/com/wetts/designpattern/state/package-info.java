/**
 * 状态模式
 * 定义：Allow an object to alter its behavior when its internal state changes. The object will appear to change its class.（当一个对象在状态改变时允许其改变行为，这个对象看起来像改变了其类。）
 * <p>
 * ---
 * 状态模式的优点：
 * 结构清晰；
 * 遵循设计原则；
 * 封装性非常好。
 * <p>
 * ---
 * 状态模式的缺点：
 * 子类太多，类膨胀。
 * <p>
 * ---
 * 状态模式的使用场景：
 * 行为随状态变化而变化的场景；
 * 条件、分支判断语句的替代者。
 *
 * @author wetts
 * @created 2015/08/25
 */
