/**
 * 抽象工厂模式（Abstract Factory Pattern）
 * 定义：Provide an interface for creating families of related or dependent objects without specifying their concrete classes.（为创建一组相关或互相依赖的对象提供一个接口，而且无需指定它们的具体类。）
 * <p>
 * ---
 * 抽象工厂模式的优点：
 * 封装性；
 * 产品族内的约束为非公开状态。
 * <p>
 * ---
 * 抽象工厂模式的缺点：
 * 产品族扩展非常困难。
 *
 * @author wetts
 * @created 2015/08/25
 */
