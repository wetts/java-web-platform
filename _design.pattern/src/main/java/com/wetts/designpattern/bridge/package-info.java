/**
 * 桥梁模式（Bridge Pattern）
 * 定义：Decouple an abstraction from its implementation so that the two can vary independently.（将抽象和实现解耦，使得两者可以独立地变化。）
 * <p>
 * ---
 * 桥梁模式的优点：
 * 抽象和实现分离；
 * 优秀的扩充能力；
 * 实现细节对客户透明。
 * <p>
 * ---
 * 桥梁模式的使用场景：
 * 不希望或不适用使用继承的场景；
 * 接口或抽象类不稳定的场景；
 * 重用性要求较高的场景。
 *
 * @author wetts
 * @created 2015/08/25
 */
